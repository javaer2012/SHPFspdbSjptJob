package com.ideal.spdb.bairong.to;

public class Location {
	private Location_homeaddr home_addr;
	private Location_bizaddr biz_addr;
	private Location_peraddr per_addr;
	private Location_applyaddr apply_addr;
	private Location_othaddr oth_addr;
	public Location_homeaddr getHome_addr() {
		return home_addr;
	}
	public void setHome_addr(Location_homeaddr home_addr) {
		this.home_addr = home_addr;
	}
	public Location_bizaddr getBiz_addr() {
		return biz_addr;
	}
	public void setBiz_addr(Location_bizaddr biz_addr) {
		this.biz_addr = biz_addr;
	}
	public Location_peraddr getPer_addr() {
		return per_addr;
	}
	public void setPer_addr(Location_peraddr per_addr) {
		this.per_addr = per_addr;
	}
	public Location_applyaddr getApply_addr() {
		return apply_addr;
	}
	public void setApply_addr(Location_applyaddr apply_addr) {
		this.apply_addr = apply_addr;
	}
	public Location_othaddr getOth_addr() {
		return oth_addr;
	}
	public void setOth_addr(Location_othaddr oth_addr) {
		this.oth_addr = oth_addr;
	}
	
}
