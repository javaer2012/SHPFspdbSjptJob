package com.ideal.spdb.bairong.to;

public class Authentication {
    private String id;
	private String cell;
	private String mail;
	private String key_relation;
	private String name;
	private String biz_tel;
	private String home_tel;
	private String id_name;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCell() {
		return cell;
	}
	public void setCell(String cell) {
		this.cell = cell;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getKey_relation() {
		return key_relation;
	}
	public void setKey_relation(String key_relation) {
		this.key_relation = key_relation;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBiz_tel() {
		return biz_tel;
	}
	public void setBiz_tel(String biz_tel) {
		this.biz_tel = biz_tel;
	}
	public String getHome_tel() {
		return home_tel;
	}
	public void setHome_tel(String home_tel) {
		this.home_tel = home_tel;
	}
	public String getId_name() {
		return id_name;
	}
	public void setId_name(String id_name) {
		this.id_name = id_name;
	}
	
}
