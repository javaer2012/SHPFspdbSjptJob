public class SysOperateLogTO extends BaseModel {
	private static final long serialVersionUID = 1L;
	private String logId;// 日志id
	private String operateDate;// 操作时间
	private String operateUser;// 操作人编号
	private String operateContent;// 操作内容
	private String happenDate;// 发生时间
	private String exceptionOutline;// 异常概要
	private String exceptionContent;// 异常内容
	private String column6;// 备份字段

	public SysOperateLogTO() {
	}

	
	
	public SysOperateLogTO(String logId, String operateDate,
			String operateUser, String operateContent, String happenDate,
			String exceptionOutline, String exceptionContent, String column6) {
		this.logId = logId;
		this.operateDate = operateDate;
		this.operateUser = operateUser;
		this.operateContent = operateContent;
		this.happenDate = happenDate;
		this.exceptionOutline = exceptionOutline;
		this.exceptionContent = exceptionContent;
		this.column6 = column6;
	}


	public String getHappenDate() {
		return happenDate;
	}

	public void setHappenDate(String happenDate) {
		this.happenDate = super.filter(happenDate);
	}

	public String getExceptionOutline() {
		return exceptionOutline;
	}

	public void setExceptionOutline(String exceptionOutline) {
		this.exceptionOutline = super.filter(exceptionOutline);
	}

	public String getExceptionContent() {
		return exceptionContent;
	}

	public void setExceptionContent(String exceptionContent) {
		this.exceptionContent = super.filter(exceptionContent);
	}

	public String getLogId() {
		return logId;
	}

	public void setLogId(String logId) {
		this.logId = super.filter(logId);
	}

	public String getOperateDate() {
		return operateDate;
	}

	public void setOperateDate(String operateDate) {
		this.operateDate = super.filter(operateDate);
	}

	public String getOperateUser() {
		return operateUser;
	}

	public void setOperateUser(String operateUser) {
		this.operateUser = super.filter(operateUser);
	}

	public String getOperateContent() {
		return operateContent;
	}

	public void setOperateContent(String operateContent) {
		this.operateContent = super.filter(operateContent);
	}

	public String getColumn6() {
		return column6;
	}

	public void setColumn6(String column6) {
		this.column6 = super.filter(column6);
	}
}