package com.ideal.spdb.system.model;

import org.directwebremoting.annotations.DataTransferObject;

import com.ideal.spdb.utils.BaseModel;

/**
 * 
 * <p>Title:  浦发五星项目</p>
 * <p>Description:子节点资源表实体</p>
 * <p>Company:上海理想信息产业(集团)有限公司</p>
 * @author wls
 * create date: 2011-8-31
 * @version 1.0
 */
@DataTransferObject 
public class SysChildnodeResourceModel extends BaseModel {
	private static final long serialVersionUID = 1L;
	private String moduleName;//模块名称
	private String systemUrl;// 路径
	private String fathernodeId;// 父节点id
	private String column4;// 备份字段

	public SysChildnodeResourceModel(String moduleName, String systemUrl,
			String fathernodeId, String column4) {
		this.moduleName = moduleName;
		this.systemUrl = systemUrl;
		this.fathernodeId = fathernodeId;
		this.column4 = column4;
	}

	public SysChildnodeResourceModel() {
	}

	
	public String getModuleName() {
		return moduleName;
	}

	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}

	public String getSystemUrl() {
		return systemUrl;
	}

	public void setSystemUrl(String systemUrl) {
		this.systemUrl = systemUrl;
	}

	public String getFathernodeId() {
		return fathernodeId;
	}

	public void setFathernodeId(String fathernodeId) {
		this.fathernodeId = fathernodeId;
	}

	public String getColumn4() {
		return column4;
	}

	public void setColumn4(String column4) {
		this.column4 = column4;
	}

}