package com.ideal.spdb.yellowpage.to;

import java.util.Date;

import com.ideal.spdb.utils.BaseModel;

/**
 * 黄页个人记录表
 * 
 * @author yxh
 */
public class YellowPageRecordModelTo extends BaseModel {
	private String yellowpage_record_id; // 唯一标志 黄页个人备份id
	private String applicationId; // 申请书id
	private String address; // 地址
	private String phone; // 电话
	private String operate_state; // 处理方式
	private String business_library; // 营业库地址
	private String business_library_calls; // 营业库电话
	private String area_code; // 区号
	private Date record_date; // 记录时间

	public String getYellowpage_record_id() {
		return yellowpage_record_id;
	}

	public void setYellowpage_record_id(String yellowpage_record_id) {
		this.yellowpage_record_id = yellowpage_record_id;
	}

	public String getApplicationId() {
		return applicationId;
	}

	public void setApplicationId(String applicationId) {
		this.applicationId = applicationId;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getOperate_state() {
		return operate_state;
	}

	public void setOperate_state(String operate_state) {
		this.operate_state = operate_state;
	}

	public String getBusiness_library() {
		return business_library;
	}

	public void setBusiness_library(String business_library) {
		this.business_library = business_library;
	}

	public String getBusiness_library_calls() {
		return business_library_calls;
	}

	public void setBusiness_library_calls(String business_library_calls) {
		this.business_library_calls = business_library_calls;
	}

	public String getArea_code() {
		return area_code;
	}

	public void setArea_code(String area_code) {
		this.area_code = area_code;
	}

	public Date getRecord_date() {
		return record_date;
	}

	public void setRecord_date(Date record_date) {
		this.record_date = record_date;
	}

}
