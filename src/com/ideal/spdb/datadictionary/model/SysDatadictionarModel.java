public class SysDatadictionarModel extends BaseModel {
	private static final long serialVersionUID = 1L;
	private String categorys;//类别
	private String clause;//字典项的条目
	private String costValue;//字典项的值
	private String childClause;//字典项值的条目
	private String childCostValue;//字典项值的值
	private String categoryDescribe;//类别描述
	private String column5;// 备份字段

	public SysDatadictionarModel() {
	}
	
	public SysDatadictionarModel(String costValue) {
		this.costValue=costValue;
	}

	public SysDatadictionarModel(String categorys, String clause,
			String costValue, String childClause, String childCostValue,
			String categoryDescribe, String column5) {
		this.categorys = categorys;
		this.clause = clause;
		this.costValue = costValue;
		this.childClause = childClause;
		this.childCostValue = childCostValue;
		this.categoryDescribe = categoryDescribe;
		this.column5 = column5;
	}

	public String getChildClause() {
		return childClause;
	}

	public void setChildClause(String childClause) {
		this.childClause = childClause;
	}

	public String getChildCostValue() {
		return childCostValue;
	}

	public void setChildCostValue(String childCostValue) {
		this.childCostValue = childCostValue;
	}

	public String getCategorys() {
		return categorys;
	}

	public void setCategorys(String categorys) {
		this.categorys = categorys;
	}

	public String getClause() {
		return clause;
	}

	public void setClause(String clause) {
		this.clause = clause;
	}

	public String getCostValue() {
		return costValue;
	}

	public void setCostValue(String costValue) {
		this.costValue = costValue;
	}

	public String getCategoryDescribe() {
		return categoryDescribe;
	}

	public void setCategoryDescribe(String categoryDescribe) {
		this.categoryDescribe = categoryDescribe;
	}

	public String getColumn5() {
		return column5;
	}

	public void setColumn5(String column5) {
		this.column5 = column5;
	}

}